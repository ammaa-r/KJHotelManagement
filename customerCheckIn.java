    public void roomDetails(){
        
        ///to automatically cange the RoomNo,RoomType,BedType,Price when Room Type or Bed Type change
        ////*********************************************************************************************
        comboCheckInRoomNo.removeAllItems();
        txtCheckInPrice.setText("");
        checkInBed=(String)comboCheckInBed.getSelectedItem();
        checkInRoomType=(String)comboCheckInRoomType.getSelectedItem();
        
        try{
            ResultSet rs=Select.getData("select * from room where bed='"+checkInBed+"' and roomType='"+checkInRoomType+"' and status='Not Booked' ");
            
            while(rs.next()){
                comboCheckInRoomNo.addItem(rs.getString(1));
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }

