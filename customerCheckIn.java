    private void btnAllocateRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAllocateRoomActionPerformed
        // TODO add your handling code here:
        int id=1;
        String name=txtCustomerName.getText();
        String mobileNumber=txtCustometMobileNo.getText();
        String address=txtCustomerAddress.getText();
        String gender=(String)comboCustomerGenter.getSelectedItem();
        String email=txtCustomerEmail.getText();
        String idProof=txtCustomerIDProof.getText();
        String checkIn=txtCheckInDate.getText();
        String bed=(String)comboCheckInBed.getSelectedItem();
        String roomType=(String)comboCheckInRoomType.getSelectedItem();
        String roomNo=(String)comboCheckInRoomNo.getSelectedItem();
        String price=txtCheckInPrice.getText();
        
        String Query=("select max(id) from hotel_customer");
        boolean validStatus1=Validations.validateEmail(email);
        boolean validStatus2=Validations.validatePhoneNo(mobileNumber);
        
        

        try{
            ResultSet rs=Select.getData(Query);
            while(rs.next()){
                id=rs.getInt(1);
            }
            id=id+1;

            ///check whether is there are any rooms to allocate
            //price field should not be emply and Phone Number& email must be validated
            if(!price.equals("") && validStatus1 && validStatus2){
                Query="update room set status='Booked' where roomNo='"+roomNo+"' ";
                InsertUpdateDelete.setData(Query, "");

                Query="insert into hotel_customer(id, name, mobileNumber, address, gender, email, idProof, checkIn, roomNo, bed, roomType, pricePerDay) values("+id+", '"+name+"', '"+mobileNumber+"', '"+address+"', '"+gender+"', '"+email+"', '"+idProof+"', '"+checkIn+"', '"+roomNo+"', '"+bed+"', '"+roomType+"', '"+price+"' ) ";
                InsertUpdateDelete.setData(Query, "Customer Check IN Successfully!!");

            }else{
                jLabel2.setText("Please check the Email and Mobile No you entered!!");
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_btnAllocateRoomActionPerformed

