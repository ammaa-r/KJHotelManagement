    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int check=0;
        String securityQuestion=txtSecirityQuestion.getText();
        String answer=txtAnswer.getText();
        String newPassword=txtNewPassword.getText();
        
        ///Validating no text field is empty
        if(answer.equals("") || newPassword.equals("")){
            check=1;
            JOptionPane.showMessageDialog(null, "All fields are required!!");
            
            //validating answer is correct or not
        }else{
            ResultSet rs=Select.getData("select * from users where email='"+email+"' and securityQuetion='"+securityQuestion+"' and answer='"+answer+"' ");
            
            try{
                if(rs.next()){
                    check=1;
                    ////update table with newPassword
                    InsertUpdateDelete.setData("update users set password='"+newPassword+"' where email='"+email+"' ", "Password Set Successfully!!");
                    setVisible(false);
                    new forgotPassword().setVisible(true);
                }
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
        if(check==0){
            JOptionPane.showMessageDialog(null, "Incorrect answer");
        }
                
    }//GEN-LAST:event_btnSaveActionPerformed

