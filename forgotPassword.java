    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        int check=0;
        email=txtEmail.getText();
        
        boolean validStatus1=Validations.validateEmail(email);
        
        if(email.equals("") || !validStatus1 ){
           check=1;
           JOptionPane.showMessageDialog(null, "Email Field should not empty!! OR \nPlease Enter a valid Email");
           
        }else{
            ResultSet rs=Select.getData("select * from users where email='"+email+"'");
            try{
                if(rs.next()){
                    check=1;
                    txtSecirityQuestion.setEditable(false);
                    txtEmail.setEditable(false);
                    txtSecirityQuestion.setText(rs.getString(4));
                }
                
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        
        if(check==0){
            JOptionPane.showMessageDialog(null, "Incorrect Email!!");
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

